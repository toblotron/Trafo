    private void jButtonCaptureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCaptureActionPerformed
        SaveRulebase();
        TrafoCore.LoadRulebase(jEditorPaneRules.getPage().getPath());
        Term result = TrafoCore.CallTrawl(jTextFieldCapture.getText(), dynamicTreeCapture, jEditorPaneTarget.getText());
        jEditorPaneOutput.setText("");
        if(jCheckBoxGenerate.isSelected())
            jEditorPaneOutput.setText(TrafoCore.Construct(result));
    }//GEN-LAST:event_jButtonCaptureActionPerformed

    public TrafoWorkbench getTrafo()
    {
        TrafoWorkbench temp = new TrafoWorkbench();
        temp.setTitle("TRAFO");
        temp.setCommand(jTextFieldCapture.getText());
        if(jEditorPaneRules.getPage() != null)
            temp.setRuleFilePath(jEditorPaneRules.getPage().getPath());
        if(jEditorPaneTarget.getPage() != null)
            temp.setSourceFilePath(jEditorPaneTarget.getPage().getPath());
        
        return temp;
    }
    
    public void setTrafo(TrafoWorkbench temp)
    {
        try{
        //temp.setTitle("TRAFO");
        jTextFieldCapture.setText(temp.getCommand());
        File file = new File(temp.getRuleFilePath());
        jEditorPaneRules.setPage(file.toURI().toURL());
        file = new File(temp.getSourceFilePath());
        jEditorPaneTarget.setPage(file.toURI().toURL());
        }catch(Exception e){}
    
    }
    
