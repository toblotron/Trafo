    private void SaveRulebase() 
    {
        try{
            FileWriter writer = null;
            writer = new FileWriter(jEditorPaneRules.getPage().getPath());
            jEditorPaneRules.write(writer);
            writer.close();
        }catch(Exception e)
        {}
    }

